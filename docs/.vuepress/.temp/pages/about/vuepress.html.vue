<h3 id="vuepress的特点" tabindex="-1"><a class="header-anchor" href="#vuepress的特点"><span>vuepress的特点</span></a></h3>
<ol>
<li>
<p>基于 Vue： VuePress 使用 Vue.js 框架，充分利用了 Vue 的组件系统和生态系统，使其易于扩展和自定义。</p>
